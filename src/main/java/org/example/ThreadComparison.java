package org.example;
/*برنامه‌ای بنویسید که دو ترد ایجاد کند:
1. یکی با استفاده از یک کلاس که از Thread ارث‌بری کرده است.
2. دیگری با استفاده از یک کلاس که Runnable را پیاده‌سازی کرده است.
• در تردها یک پیغام ساده را در یک حلقه چاپ کنید و تفاوت‌ها را بررسی کنید.
*/
public class ThreadComparison {
    public static void main(String[] args) {

        MyThread thread1 = new MyThread();
        thread1.start();

        Thread thread2 = new Thread(new MyRunnable());
        thread2.start();

        System.out.println("Main thread is finished.");
    }
}
/*
 * توضیحات کد:
ترد اول (MyThread):

کلاسی تعریف شده که از Thread ارث‌بری می‌کند.
متد run() برای اجرای کد داخل ترد بازنویسی شده است.
هر بار در حلقه یک پیام چاپ می‌کند و 500 میلی‌ثانیه مکث می‌کند.

* ترد دوم (MyRunnable):

کلاسی تعریف شده که اینترفیس Runnable را پیاده‌سازی می‌کند.
متد run() همانند ترد اول پیام چاپ می‌کند.
این کلاس با new Thread() برای ایجاد یک ترد استفاده می‌شود.

اجرای تردها:

هر دو ترد به صورت مستقل و همزمان اجرا می‌شوند.
پیام‌های آن‌ها به دلیل اجرای موازی ممکن است در خروجی به ترتیب خاصی نباشند.

*/